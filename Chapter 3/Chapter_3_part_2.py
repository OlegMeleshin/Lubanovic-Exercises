# 4. Create a list called "things" containing three items:
# "mozzarella", "cinderella", "salmonella".

things = ['mozzarella', 'cinderella', 'salmonella']
print(things)



# 5. Write a word related to a person with a capital letter then
# print the list. Has the item changed?

things[1] = things[1].capitalize()
print(f"\n{things[1]} Cause she`s a humanbeing after all")
print(things)



# 6. Convert the whole cheesy item to uppercase and print the list.

things[0] = things[0].upper()
print(f"\n{things[0]} Cause we`re hungry")
print(things)



# 7. Remove a disease out of the list, get a Nobel prize,
# then print the list.

things.remove('salmonella')
print(f"\n{things} Cause we`ve found the Cure!\n")












































