'''1. Assign value 7 to a variable guess_me. Make conditional checks (if, elif, else)
to print the string 'too low' if value is lesser than 7, 'too high' if it`s bigger,
'just right' if it`s equal 7.'''
guess_me = 7
if guess_me < 7:
    print("too low")
elif guess_me > 7:
    print("too high")
else:
    print("just right")